public class Task1 {
    public static void main(String[] args) {
        int x = 5;
        int y = 7;

        System.out.println(x+y);   // в выражениях используются целые числа
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);

        double x1 = 5;
        double y1= 7;

        System.out.println(x1+y1);  // в выражениях используются числа с плавающей точкой
        System.out.println(x1-y1);
        System.out.println(x1*y1);
        System.out.println(x1/y1);
    }
}
